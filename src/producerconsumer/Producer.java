    @Override
    public void run() {
        Random producer = new Random(0);
        int value = 0, sleepTime;
        
        jTextArea1.setEditable(false);
        do {
            try {
                value = producer.nextInt() % 0xf;
                queue.put(value);
                jTextArea1.append("producing: " + String.valueOf(value)+"\n");
                sleepTime = Math.abs(producer.nextInt() % 4);
                Thread.sleep(500*sleepTime);
            } catch (InterruptedException ex) {
                Logger.getLogger(Producer.class.getName()).log(Level.SEVERE, null, ex);
            }
        } while(value != 0);
    }
